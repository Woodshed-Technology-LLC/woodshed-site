// Map the colors from the Figma Tokens Studio plugin into the format that Tailwind expects. The
// strings are not perfectly formatted in terms of casing, but that's easy to fix manually.
// We don't use this in the application code; it's just used for copying the tokens over into
// tailwind.config.js.
function main() {
    const result = {};

    Object.values(colors).forEach(layer0 => {
        Object.values(layer0).forEach(layer1 => {
            Object.entries(layer1).forEach(([key, value]) => {
                const keys = key.split(' ');
                const family = keys[0].trim();
                const grade = parseInt(keys[1].trim());

                if (result[family] === undefined) {
                    result[family] = {};
                }

                result[family][grade] = value.value;
            });
        });
    });

    console.log(JSON.stringify(result, null, 4));
}

main();
